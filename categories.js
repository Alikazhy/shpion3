// Пользовательские слова (загружаются из localStorage)
let CUSTOM_CATEGORIES = {};

function loadCustomCategories() {
  try {
    const saved = localStorage.getItem('spy_custom_categories');
    CUSTOM_CATEGORIES = saved ? JSON.parse(saved) : {};
  } catch { CUSTOM_CATEGORIES = {}; }
}

function saveCustomCategories() {
  localStorage.setItem('spy_custom_categories', JSON.stringify(CUSTOM_CATEGORIES));
}

function getAllCategories() {
  return { ...CATEGORIES, ...CUSTOM_CATEGORIES };
}

function getTotalWordCount() {
  return Object.values(getAllCategories()).reduce((sum, c) => sum + (c.words?.length || 0), 0);
}

